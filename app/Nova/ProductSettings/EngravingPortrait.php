    {
        return [
            Text::make('Цена', 'price')
                ->sortable(),
            BelongsTo::make('Размер', 'size', 'App\Nova\ProductSettings\EngravingPortraitSize'),
            BelongsTo::make('Тип', 'type', 'App\Nova\ProductSettings\EngravingPortraitType')
        ];
    }

    /**
     * Get the cards available for the request.
     *
     * @param  \Illuminate\Http\Request  $request
